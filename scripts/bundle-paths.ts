import fs from "fs";
import path from "path";
import { getAllPathsFromMdx } from "../src/lib/paths/mdxParser";
import { assertLocaleIdParity } from "./lib/validateLocaleParity";
import { formatWithPrettier } from "./lib/formatWithPrettier";

const en = getAllPathsFromMdx("en");
const es = getAllPathsFromMdx("es");

assertLocaleIdParity(en, es, "paths");

const dataDir = path.join(process.cwd(), "src", "data");
const header = `// Auto-generated by scripts/bundle-paths.ts — do not edit by hand.\n// Edit content/paths/{en,es}/*.mdx then run: npm run content:bundle-paths\n\nimport type { LearningPath } from "@/types/learningPath";\n\n`;

const writeLocaleBundle = (locale: "en" | "es", paths: typeof en) => {
  const outPath = path.join(dataDir, `pathBundles.${locale}.ts`);
  fs.writeFileSync(
    outPath,
    `${header}export const paths: LearningPath[] = ${JSON.stringify(paths, null, 2)} as const;\n`,
    "utf8"
  );
  formatWithPrettier(outPath);
  return outPath;
};

writeLocaleBundle("en", en);
writeLocaleBundle("es", es);

fs.writeFileSync(
  path.join(dataDir, "pathBundles.ts"),
  `${header}import { paths as en } from "./pathBundles.en";\nimport { paths as es } from "./pathBundles.es";\n\nexport const pathBundles: Record<"en" | "es", LearningPath[]> = { en, es } as const;\n`,
  "utf8"
);

console.log(`Wrote path bundles (${en.length} EN, ${es.length} ES paths).`);
