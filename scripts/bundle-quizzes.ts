import fs from "fs";
import path from "path";
import { execFileSync } from "child_process";
import { getAllQuizzesFromMdx } from "../src/lib/quizzes/quizParser";
import { assertLocaleIdParity } from "./lib/validateLocaleParity";

const en = getAllQuizzesFromMdx("en");
const es = getAllQuizzesFromMdx("es");

assertLocaleIdParity(en, es, "quizzes");

const dataDir = path.join(process.cwd(), "src", "data");
const header = `// Auto-generated by scripts/bundle-quizzes.ts — do not edit by hand.\n// Edit content/quizzes/{en,es}/*.mdx then run: npm run content:bundle-quizzes\n\nimport type { Quiz } from "@/types/quiz";\n\n`;

const writeLocaleBundle = (locale: "en" | "es", quizzes: typeof en) => {
  const outPath = path.join(dataDir, `quizBundles.${locale}.ts`);
  fs.writeFileSync(
    outPath,
    `${header}export const quizzes: Quiz[] = ${JSON.stringify(quizzes, null, 2)} as const;\n`,
    "utf8"
  );
  execFileSync("npx", ["prettier", "--write", outPath], { shell: process.platform === "win32" });
  return outPath;
};

writeLocaleBundle("en", en);
writeLocaleBundle("es", es);

const indexPath = path.join(dataDir, "quizBundles.ts");
fs.writeFileSync(
  indexPath,
  `${header}import { quizzes as en } from "./quizBundles.en";\nimport { quizzes as es } from "./quizBundles.es";\n\nexport const quizBundles: Record<"en" | "es", Quiz[]> = { en, es } as const;\n`,
  "utf8"
);

console.log(`Wrote quiz bundles (${en.length} EN, ${es.length} ES quizzes).`);
