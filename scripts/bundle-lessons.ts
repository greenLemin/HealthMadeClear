import fs from "fs";
import path from "path";
import { execSync } from "child_process";
import { getAllLessonsFromMdx } from "../src/lib/lessons/mdxParser";
import { assertLocaleIdParity } from "./lib/validateLocaleParity";
import { execSync } from "child_process";

const en = getAllLessonsFromMdx("en");
const es = getAllLessonsFromMdx("es");

assertLocaleIdParity(en, es, "lessons");

const dataDir = path.join(process.cwd(), "src", "data");
const header = `// Auto-generated by scripts/bundle-lessons.ts — do not edit by hand.\n// Edit content/lessons/{en,es}/*.mdx then run: npm run content:bundle-lessons\n\nimport type { Lesson } from "@/types/lesson";\n\n`;

const writeLocaleBundle = (locale: "en" | "es", lessons: typeof en) => {
  const outPath = path.join(dataDir, `lessonBundles.${locale}.ts`);
  fs.writeFileSync(
    outPath,
    `${header}export const lessons: Lesson[] = ${JSON.stringify(lessons, null, 2)} as const;\n`,
    "utf8"
  );
  execSync(`npx prettier --write ${outPath}`);
  return outPath;
};

writeLocaleBundle("en", en);
writeLocaleBundle("es", es);

const indexPath = path.join(dataDir, "lessonBundles.ts");
fs.writeFileSync(
  indexPath,
  `${header}import { lessons as en } from "./lessonBundles.en";\nimport { lessons as es } from "./lessonBundles.es";\n\nexport const lessonBundles: Record<"en" | "es", Lesson[]> = { en, es } as const;\n`,
  "utf8"
);

console.log(`Wrote lesson bundles (${en.length} EN, ${es.length} ES lessons).`);
